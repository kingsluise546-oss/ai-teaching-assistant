    "[[ITEM_START:1]]",
    "[[KP:多音字|字音字形]]",
    "[[ANS]]B[[/ANS]]",
    "[[EXP]]翘首的翘读qiáo，悄然的悄读qiǎo[[/EXP]]",
    "[[ITEM_END:1]]",
    "",
    "[[ITEM_START:2]]",
    "[[KP:形近字|字音字形]]",
    "[[ANS]]D[[/ANS]]",
    "[[EXP]]A项计较误为计效，B项再接再厉误为再接再励[[/EXP]]",
