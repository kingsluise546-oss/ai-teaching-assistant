/** 预留：技能调用参数 — 以后路由系统从这里传 */
export interface SkillOptions {
  subject?: string;
  stage?: string;
  grade?: string;
  lessonType?: string;
}

/** 预留：备课助手的学科特化配置 */
interface SubjectConfig {
  name: string;
  framework: string;
  emphasis: string[];
}

/** 预留：学科配置注册表 */
const subjectConfigs: Record<string, SubjectConfig> = {};

export function registerSubject(config: SubjectConfig) {
  subjectConfigs[config.name] = config;
}

export function getSubjectConfig(name: string): SubjectConfig | undefined {
  return subjectConfigs[name];
}

