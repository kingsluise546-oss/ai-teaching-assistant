/** 根据年级选取最匹配的教案范本（节省 ~400 行/次，约 75% token） */
function selectLessonExample(grade?: string): string {
  if (!grade) return LESSON_EXAMPLE_小学故事; // 默认
  const g = grade.trim();
  // 初中及以上 → 诫子书（论述类）
  if (g.includes("初") || g.includes("七") || g.includes("八") || g.includes("九")) {
    return LESSON_EXAMPLE_初中;
  }
  // 小学 → 草船借箭（故事类，普适性最强）
  return LESSON_EXAMPLE_小学故事;
}

export function prepareLessonPrompt(topic: string, options?: SkillOptions) {
  const example = selectLessonExample(options?.grade);
  const stageHint = options?.grade
    ? (options.grade.includes("初") || options.grade.includes("七") || options.grade.includes("八") || options.grade.includes("九")
      ? "初中" : "小学")
    : "小学";

  return {
    system: `你是一位具有10年以上${stageHint}语文教学经验的骨干教师。请严格按照以下教案的质量水准和格式撰写。直接输出正文，禁止开场白。

【格式要求】
三级编号：一、（一）1.。不同标签之间必须空一行。课题、年级、课时用 | 分隔在同一行。禁止使用markdown表格。

【教学设计决策链——五步，不可跳过】
第一步：读懂课文最独特之处。这篇课文最打动人的是什么？最值得教的是什么？
第二步：确定怎么让学生自己发现。1-3年级可用句式填空（如"只要______，______就能______"）；4年级及以上禁止任何句式填空，一律用开放式表达、固定句式汇报、书面批注。
第三步：预判学生卡点。据此设计追问链或认知冲突。
第四步：检查每个环节。教师活动写具体操作，学生活动写具体行为。杜绝空话套话。
第五步：写应急方案。给出具体调整方案，不能写"根据课堂情况灵活调整"。

【执行标准七条】
1. 核心问题驱动 2. 文本证据原则 3. 追问链设计（按需） 4. 认知冲突制造（按需） 5. 过渡语功能化 6. 活动行为化 7. 反思实战化

【质量标准】
1. 教学目标：每维度2条，用"能说出/能写出/能找出/能概括/学会/感受/理解"开头
2. 教学重点/难点：各一句完整的话
3. 教学准备：**教师：**和**学生：**加粗分行
4. 教学过程：五环节标注时长，精读探究2-3子活动，追问链至少2层，过渡语承上启下
5. 板书设计：代码块，箭头对齐
6. 作业设计：三层，每层标注时限
7. 教学反思：具体困难预判+应急方案；课后反思留白

${example}`,
    messages: [{ role: "user" as const, content: `课题：${topic}\n请生成一份完整的教案。` }],
  };
}

export function generateQuestionsPrompt(params: {
  subject: string;
  grade: string;
  topic: string;
  type?: string;
  difficulty: string;
  count: number;
}) {
  const stage = detectStage(params.grade);
  const difficulty = (params.difficulty as Difficulty) || "中等";
  const subject = params.subject as import("./routingTree").Subject;

  // 严格降级：指定了题型但未注册规则 → 拒绝生成
  if (params.type && !findRule(stage, subject, params.type)) {
    return {
      system: "你是一位K12命题专家。",
      messages: [
        {
          role: "user" as const,
          content: `【该题型暂未完成规则注册】\n学段：${stage} | 学科：${subject} | 题型：${params.type}\n该题型路由已存在但规则尚未编写，请勿自由生成，避免质量失控。`,
        },
      ],
    };
  }

  const rule = params.type ? findRule(stage, subject, params.type) : undefined;

  // 年级语言控制
  const gradeLang = getGradeLanguage(params.grade);
  const difficultyGuide = buildDifficultyText(difficulty, params.grade, params.type || "题目");

  // 构建模块化 Prompt
  const systemPrompt = [
    `你是一位K12命题专家。直接输出题目，禁止任何开场白、问候语或角色确认（如"好的""作为命题专家""我将按要求生成"等）。年级=${params.grade}，学段=${stage}。`,
    "",
    "【通用难度框架】",
    difficultyGuide,
    "",
    "【年级语言控制】",
    `句子长度：${gradeLang.sentenceLength}`,
    `词汇水平：${gradeLang.vocabulary}`,
    `语境风格：${gradeLang.contextStyle}`,
    `句法复杂度：${gradeLang.syntaxLevel}`,
    "",
    "【学段要求】",
    stage === "小学"
      ? (subject === "英语" ? "重识记与准确性，用简单句（5-10词），情境具象化（图片匹配、判断对错），语言亲切。" :
         subject === "数学" ? "重基础运算和概念理解，一步到位，考查准确度。" :
         "重识记理解，有趣味有情境，语言亲切。")
      : "",
    stage === "初中"
      ? (subject === "英语" ? "重语境化应用，用句群（2-3句），真实情境中运用语言，加强逻辑连接词（because, so, however）。" :
         subject === "数学" ? "重公式运用和逻辑推理，跨知识点综合。" :
         "重理解分析，有思维含量，使用学科术语。")
      : "",
    stage === "高中"
      ? (subject === "英语" ? "重批判性思维与思辨，用段落（5-7句），选材涉及科技伦理、传统文化、中国成就。" :
         subject === "数学" ? "重抽象思维和综合应用，接近高考压轴难度。" :
         "重综合思辨，近高考风格，强调知识整合。")
      : "",
    "",
    "【格式——参照中高考真题标准】",
    "1. 题目区与答案区用 --- 分隔（--- 前后各空一行）",
    "2. 生成多道题时，## 标题后加（一）（二）区分，每题之间用 --- 分隔",
    "3. 序号与内容同行，不换行。大题：一、二、三、 子题：1. 2. 3.",
    "4. 选择题选项各占一行：A. ...  B. ...  C. ...  D. ...",
    "5. 分值标注在题末括号内，如（3分）。默写类空白用中文引号包裹全角空格",
    "6. 数学公式禁止LaTeX（\\frac \\sqrt \\cos \\theta 等），用纯文本：分数a/b，根号√2，sin30°",
    "7. 答案区格式（**答案与解析** 必须加粗，考点答案解析三词加粗带冒号）：",
    "   **答案与解析**",
    "",
    "   **1. 考点：**xxx",
    "   **答案：**xxx",
    "   **解析：**xxx",
    "",
    "   **2. 考点：**xxx",
    "   **答案：**xxx",
    "   **解析：**xxx",
    "8. 禁止 [[ ]] 双括号标记，禁止【第一篇】等标签，禁止 ==== 分隔线",
  ];

  if (rule) {
    systemPrompt.push(
      "",
      `【题型：${rule.typeName}】`,
      `考查目标：${rule.objective}`,
      "",
      `【材料规格】${rule.materialSpec}`,
      "",
      `【题目结构】${rule.questionStructure}`,
      "",
      `【答案格式】${rule.answerFormat}`,
      "",
      `【难度控制（${difficulty}）】${rule.difficultyControl[difficulty]}`,
      "",
      "【约束条件】",
      ...rule.constraints.map((c, i) => `${i + 1}. ${c}`),
      "",
      "【输出模板——严格遵循格式，禁止使用 **答案：** 等旧格式】",
      rule.outputTemplate,
    );
  } else {
    systemPrompt.push(
      "",
      `题型：${params.type || "请根据学段和知识点选择合适题型"}`,
      "每题输出：题号、题干、答案、解析、考点标签。",
    );
  }

  // 输出规则
  systemPrompt.push(
    "",
    "【输出规则】直接输出题目正文，禁止任何开场白。答案区使用 **N. 考点：** / **答案：** / **解析：** 格式。",
  );

  // 阅读类（multiLayer）：count = 完整篇章数，每篇自带固定子题结构
  const isMultiLayer = rule?.answerStructure === "multiLayer";
  const countLabel = isMultiLayer ? "篇" : "道";
  const countHint = isMultiLayer
    ? `（每篇按题型结构自带子题，共${params.count}篇完整阅读）`
    : "";
  const userMsg = `请生成${params.count}${countLabel}${params.grade}${subject}关于"${params.topic}"的题目，难度${difficulty}。${params.type ? `题型：${params.type}` : ""}${countHint}\n（按真题格式输出，答案区使用 **考点：**/**答案：**/**解析：** 格式）`;
  return {
    system: systemPrompt.join("\n"),
    messages: [
      {
        role: "user" as const,
        content: userMsg,
      },
    ],
  };
}

